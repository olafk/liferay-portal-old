	public static void generateTypeScriptClient(
			File configDir, ConfigYAML configYAML, File copyrightFile,
			String yamlString)
		throws Exception {

		File baseClientDir = new File(
			StringUtil.removeLast(configDir.getPath(), "-impl") + "-client-js");

		List<File> files = new ArrayList<>();

		_createBuildGradleFile(baseClientDir, files);
		_createNodeScriptsConfigFile(baseClientDir, files);
		_createPackageJSONFile(baseClientDir, files);

		OpenAPIYAML openAPIYAML = OpenAPIParserUtil.loadOpenAPIYAML(
			_addAutogeneratedVulcanSchemas(configYAML, yamlString));

		Map<String, Map<String, Object>> contextMap = _buildContextMap(
			configYAML, openAPIYAML);

		Set<Map.Entry<String, Map<String, Object>>> entries =
			contextMap.entrySet();

		for (Map.Entry<String, Map<String, Object>> entry : entries) {
			_createFile(
				entry.getValue(), configYAML, copyrightFile, files,
				"typescript/api",
				StringBundler.concat(
					baseClientDir.getPath(), "/src/node/api/",
					StringUtil.lowerCaseFirstLetter(entry.getKey()), "Api.ts"));
		}

		_createFile(
			null, configYAML, copyrightFile, files, "typescript/api_global",
			baseClientDir.getPath() + "/src/node/api.ts");
		_createFile(
			_buildApisContext(entries), configYAML, copyrightFile, files,
			"typescript/apis",
			baseClientDir.getPath() + "/src/node/api/apis.ts");

		Components components = openAPIYAML.getComponents();

		Map<String, Schema> schemaMap = components.getSchemas();

		if (schemaMap != null) {
			for (Map.Entry<String, Schema> entry : schemaMap.entrySet()) {
				_createFile(
					_buildModelContext(entry.getKey(), entry.getValue()),
					configYAML, copyrightFile, files, "typescript/model",
					StringBundler.concat(
						baseClientDir.getPath(), "/src/node/model/",
						StringUtil.lowerCaseFirstLetter(entry.getKey()),
						".ts"));
			}
		}

		_createFile(
			Collections.singletonMap("schemaMap", schemaMap), configYAML,
			copyrightFile, files, "typescript/models",
			baseClientDir.getPath() + "/src/node/model/models.ts");

		FileUtil.deleteFiles(baseClientDir.getPath(), files);
	}

	private static String _addAutogeneratedVulcanSchemas(
			ConfigYAML configYAML, String yamlString)
		throws Exception {

		OpenAPIYAML openAPIYAML = OpenAPIParserUtil.loadOpenAPIYAML(yamlString);
		Set<String> processedSchemaNames = new HashSet<>();

		FreeMarkerTool freeMarkerTool = FreeMarkerTool.getInstance();

		Map<String, Schema> schemas = freeMarkerTool.getSchemas(openAPIYAML);

		for (String schemaName : schemas.keySet()) {
			List<JavaMethodSignature> javaMethodSignatures =
				freeMarkerTool.getResourceJavaMethodSignatures(
					configYAML, openAPIYAML, schemaName);

			for (JavaMethodSignature javaMethodSignature :
					ResourceOpenAPIParser.
						getResourceGetPageJavaMethodSignatures(
							javaMethodSignatures)) {

				String returnType = StringUtil.removeSubstrings(
					javaMethodSignature.getReturnType(),
					"com.liferay.portal.vulcan.pagination.Page<", ">");

				String returnSchemaName = StringUtil.extractLast(
					returnType, ".");

				if (processedSchemaNames.add(returnSchemaName)) {
					if (processedSchemaNames.add("Facet")) {
						yamlString = _addSchema(
							FreeMarkerUtil.processTemplate(
								null, null, "facet_yaml", null),
							yamlString);
					}

					yamlString = _addSchema(
						FreeMarkerUtil.processTemplate(
							null, null, "page_yaml",
							HashMapBuilder.<String, Object>put(
								"schemaName", returnSchemaName
							).build()),
						yamlString);

					if (StringUtil.equals(
							"com.liferay.portal.vulcan.permission.Permission",
							returnType)) {

						yamlString = _addSchema(
							FreeMarkerUtil.processTemplate(
								null, null, "permission_yaml", null),
							yamlString);
					}
				}

				int index = StringUtil.indexOfAny(
					yamlString,
					new String[] {
						"\"" + javaMethodSignature.getPath() + "\":",
						" " + javaMethodSignature.getPath() + ":"
					});

				String httpMethod = OpenAPIParserUtil.getHTTPMethod(
					javaMethodSignature.getOperation());

				index = yamlString.indexOf(httpMethod + ":", index);

				String oldYAMLString = yamlString.substring(
					index, yamlString.indexOf("tags:", index));

				String newYAMLString = oldYAMLString.replaceAll(
					StringBundler.concat(
						"schema:\n([ \t]+)items:\n[ \t]+",
						"\\$ref: \"#/components/schemas/", returnSchemaName,
						"\"\n[ \t]+type: array"),
					StringBundler.concat(
						"schema:\n$1\\$ref: \"#/components/schemas/Page",
						returnSchemaName, "\""));

				yamlString = StringUtil.replace(
					yamlString, oldYAMLString, newYAMLString);
			}
		}

		return yamlString;
	}

	private static String _addSchema(
		String schemaYAMLString, String yamlString) {

		schemaYAMLString = StringUtil.replace(
			schemaYAMLString, new String[] {"$", "\t", "\n"},
			new String[] {"\\$", "$1", "\n$1$1"});

		return yamlString.replaceAll(
			"([ \\t]+)schemas:", "$1schemas:\n$1$1" + schemaYAMLString);
	}

	private static Map<String, Object> _buildApisContext(
		Set<Map.Entry<String, Map<String, Object>>> entries) {

		List<Map<String, Object>> apiContexts = new ArrayList<>();

		ArrayList<Map.Entry<String, Map<String, Object>>> entriesList =
			new ArrayList<>(entries);

		entriesList.sort(Map.Entry.comparingByKey());

		for (Map.Entry<String, Map<String, Object>> entry : entriesList) {
			apiContexts.add(entry.getValue());
		}

		return Collections.singletonMap("apiContexts", apiContexts);
	}

	private static Map<String, Map<String, Object>> _buildContextMap(
		ConfigYAML configYAML, OpenAPIYAML openAPIYAML) {

		Map<String, PathItem> pathItems = openAPIYAML.getPathItems();

		Map<String, List<Map<String, Object>>> operationsMap = new HashMap<>();

		for (Map.Entry<String, PathItem> entry : pathItems.entrySet()) {
			for (Operation operation :
					OpenAPIParserUtil.getOperations(entry.getValue())) {

				List<String> tags = operation.getTags();

				List<Map<String, Object>> operations =
					operationsMap.computeIfAbsent(
						tags.get(0), k -> new ArrayList<>());

				operations.add(
					_buildOperationMap(
						configYAML, openAPIYAML, operation, entry.getKey()));
			}
		}

		Map<String, Map<String, Object>> apiContexts = new HashMap<>();

		for (Map.Entry<String, List<Map<String, Object>>> entry :
				operationsMap.entrySet()) {

			Map<String, Object> apiContext = HashMapBuilder.<String, Object>put(
				"classname", entry.getKey() + "Api"
			).put(
				"operations", entry.getValue()
			).build();

			Set<Map<String, String>> allImports = new LinkedHashSet<>();

			for (Map<String, Object> tagOperation : entry.getValue()) {
				if (tagOperation.containsKey("imports")) {
					Collection<Map<String, String>> imports =
						(Collection<Map<String, String>>)tagOperation.remove(
							"imports");

					allImports.addAll(imports);
				}
			}

			if (!allImports.isEmpty()) {
				apiContext.put("imports", allImports);
			}

			apiContexts.put(entry.getKey(), apiContext);
		}

		return apiContexts;
	}

	private static Map<String, Object> _buildModelContext(
		String modelName, Schema schema) {

		Map<String, Object> modelContext = HashMapBuilder.<String, Object>put(
			"description", schema.getDescription()
		).put(
			"modelName", modelName
		).build();

		if (schema.getDiscriminator() != null) {
			Discriminator discriminator = schema.getDiscriminator();

			String discriminatorPropName = discriminator.getPropertyName();

			if (Validator.isNotNull(discriminatorPropName)) {
				modelContext.put("discriminator", discriminatorPropName);
			}
		}

		Set<Map<String, String>> imports = new HashSet<>();

		List<Map<String, Object>> schemaProperties = new ArrayList<>();

		if (schema.getAllOfSchemas() != null) {
			List<Schema> allOfSchemas = schema.getAllOfSchemas();

			Schema parentSchema = allOfSchemas.get(0);

			if (parentSchema.getReference() != null) {
				String parentSchemaReference = parentSchema.getReference();

				String parentClass = parentSchemaReference.substring(
					parentSchemaReference.lastIndexOf('/') + 1);

				modelContext.put("parent", parentClass);
				imports.add(Collections.singletonMap("classname", parentClass));

				for (Schema additionalSchema : schema.getAllOfSchemas()) {
					if (additionalSchema.getPropertySchemas() != null) {
						Map<String, Schema> propertySchemas =
							additionalSchema.getPropertySchemas();

						propertySchemas.forEach(
							(propName, propSchema) -> schemaProperties.add(
								HashMapBuilder.<String, Object>put(
									"name",
									StringUtil.replace(
										propName, '-', StringPool.UNDERLINE)
								).put(
									"type",
									_getTypeScriptType(propSchema, imports)
								).build()));
					}
				}
			}
		}

		if (schema.getPropertySchemas() != null) {
			Map<String, Schema> propertySchemas = schema.getPropertySchemas();

			propertySchemas.forEach(
				(propName, propSchema) -> schemaProperties.add(
					HashMapBuilder.<String, Object>put(
						"name",
						StringUtil.replace(propName, '-', StringPool.UNDERLINE)
					).put(
						"type", _getTypeScriptType(propSchema, imports)
					).build()));
		}

		modelContext.put("properties", schemaProperties);

		if (!imports.isEmpty()) {
			modelContext.put("imports", new ArrayList<>(imports));
		}

		return modelContext;
	}

	private static Map<String, Object> _buildOperationMap(
		ConfigYAML configYAML, OpenAPIYAML openAPIYAML, Operation operation,
		String path) {

		Map<String, Object> operationMap = HashMapBuilder.<String, Object>put(
			"httpMethod",
			StringUtil.toUpperCase(OpenAPIParserUtil.getHTTPMethod(operation))
		).put(
			"nickname", operation.getOperationId()
		).put(
			"notes", operation.getDescription()
		).put(
			"path",
			StringBundler.concat(
				configYAML.getApplication(
				).getBaseURI(),
				"/",
				openAPIYAML.getInfo(
				).getVersion(),
				path)
		).build();

		Map<ResponseCode, Response> responses = operation.getResponses();

		if (responses != null) {
			Set<String> produces = new LinkedHashSet<>();

			for (Response response : responses.values()) {
				Map<String, Content> content = response.getContent();

				if (content != null) {
					produces.addAll(content.keySet());
				}
			}

			if (!produces.isEmpty()) {
				operationMap.put("produces", new ArrayList<>(produces));
			}
		}

		Set<Map<String, String>> imports = new HashSet<>();

		Collection<Map<String, Object>> operationParams =
			_getAllOperationParams(operation, operationMap, imports);

		if (!operationParams.isEmpty()) {
			operationMap.put("allParams", operationParams);
		}

		String returnType = null;

		if (responses != null) {
			for (Map.Entry<ResponseCode, Response> entry :
					responses.entrySet()) {

				ResponseCode responseCode = entry.getKey();

				if (Objects.equals(responseCode.getHttpCode(), 200)) {
					Response response = entry.getValue();

					Map<String, Content> contentMap = response.getContent();

					if ((contentMap != null) && !contentMap.isEmpty()) {
						Collection<Content> contents = contentMap.values();

						Iterator<Content> iterator = contents.iterator();

						Content firstContent = iterator.next();

						if ((firstContent != null) &&
							(firstContent.getSchema() != null)) {

							returnType = _getTypeScriptType(
								firstContent.getSchema(), imports);
						}
					}

					break;
				}
			}
		}

		operationMap.put("returnType", returnType);

		if (!imports.isEmpty()) {
			operationMap.put("imports", new ArrayList<>(imports));
		}

		return operationMap;
	}

	private static void _createBuildGradleFile(
			File baseClientDir, List<File> files)
		throws Exception {

		File file = new File(baseClientDir, "build.gradle");

		files.add(file);

		FileUtil.write(file, StringPool.BLANK);
	}

	private static void _createFile(
			Map<String, Object> context, ConfigYAML configYAML,
			File copyrightFile, List<File> files, String name, String path)
		throws Exception {

		File file = new File(path);

		files.add(file);

		if (context != null) {
			context = HashMapBuilder.<String, Object>putAll(
				context
			).put(
				"configYAML", configYAML
			).build();
		}
		else {
			context = Collections.singletonMap("configYAML", configYAML);
		}

		FileUtil.write(
			file,
			FreeMarkerUtil.processTemplate(
				copyrightFile, FileUtil.getCopyrightYear(file), name, context));
	}

